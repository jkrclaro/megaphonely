    def parse(self, response):
        soup = BeautifulSoup(response.body, 'lxml')
        tds = soup.find_all('td')
        iteration = 0
        businesses = []
        business = {}
        for i, td in enumerate(tds):
            if i not in (0, 1):
                if td.text and td.text != ' ':
                    iteration += 1
                    text = td.text.replace('\r', '')
                    if iteration == 1:
                        business['name'] = text
                    elif iteration == 2:
                        business['category'] = text
                    elif iteration == 3:
                        business['area'] = text
                    elif iteration == 4:
                        business['phone'] = text
                        print('appending:', business)
                        businesses.append(business)
                        business = {}
                        iteration = 0

        for business in businesses:
            local_page_item = LocalPageItem(
                name=business['name'],
                category=business['category'],
                area=business['area'],
                phone=business['phone']
            )
            yield local_page_item
